"""
四种 Prompt 策略模板：Direct / RaR / CoT / RaR+CoT
"""

# ============================================================
# 策略1: Direct (直接回答)
# ============================================================
DIRECT_PROMPT = """直接给出答案，不解释、不推理。只输出答案本身。

{question}"""

# ============================================================
# 策略2: RaR (Rephrase and Respond — 先重述再回答)
# ============================================================
RAR_PROMPT = """你的任务是先重新表述用户的问题，然后回答重新表述后的问题。

具体步骤：
1. 仔细阅读用户的问题，识别其中可能存在的歧义、模糊之处、缺失信息或隐含假设
2. 将问题重新表述为一个更清晰、更具体、信息更完整的版本，消除可能的误解
3. 基于重新表述后的问题给出回答

请按以下格式输出：
【重述问题】<重新表述后的问题>
【回答】<你的回答>

用户问题：{question}"""

# ============================================================
# 策略5: Rephrase Only (仅重述，不回答 — 用于 Two-Step RaR)
# ============================================================
REPHRASE_ONLY_PROMPT = """你的任务是把用户的问题重新表述得更清晰。只输出重述后的问题，不要回答。

具体步骤：
1. 识别问题中的歧义、模糊之处、缺失信息或隐含假设
2. 将问题重新表述为一个更清晰、更具体、信息更完整的版本
3. 消除可能的误解

只输出重述后的问题文本，不要加任何前缀、标签或解释。

用户问题：{question}"""

# ============================================================
# 策略3: CoT (Chain of Thought — 一步步推理)
# ============================================================
COT_PROMPT = """请逐步推理并回答以下问题。

请按以下格式输出：
【推理过程】<你的逐步推理>
【最终答案】<你的答案>

问题：{question}"""

# ============================================================
# 策略4: RaR + CoT (重述 + 逐步推理)
# ============================================================
RAR_COT_PROMPT = """你的任务是先重新表述用户的问题，然后通过逐步推理来回答重新表述后的问题。

具体步骤：
1. 仔细阅读用户的问题，识别其中可能存在的歧义、模糊之处、缺失信息或隐含假设
2. 将问题重新表述为一个更清晰、更具体、信息更完整的版本
3. 针对重新表述后的问题，进行逐步推理
4. 给出最终答案

请按以下格式输出：
【重述问题】<重新表述后的问题>
【推理过程】<一步一步的推理>
【最终答案】<你的答案>

用户问题：{question}"""

# ============================================================
# 策略5: Rephrase Only (仅重述，不回答 — 用于 Two-Step RaR)
# ============================================================
REPHRASE_ONLY_PROMPT = """你的任务是把用户的问题重新表述得更清晰。只输出重述后的问题，不要回答。

具体步骤：
1. 识别问题中的歧义、模糊之处、缺失信息或隐含假设
2. 将问题重新表述为一个更清晰、更具体、信息更完整的版本
3. 消除可能的误解

只输出重述后的问题文本，不要加任何前缀、标签或解释。

用户问题：{question}"""
